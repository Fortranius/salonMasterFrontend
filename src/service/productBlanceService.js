export function getAllProductsBalance() {
    return fetch("http://localhost:8080/api/getAllProductsBalance")
        .then(handleErrors)
        .then(res => res.json())
        .then(val => {
            return val;
        });
}

function handleErrors(response) {
    if (!response.ok) {
        throw Error(response.statusText);
    }
    return response;
}